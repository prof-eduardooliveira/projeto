    private void jbBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscaActionPerformed
        if (jRBNome.isSelected()) {
            modelUsuario.setNome(jtPesquisa.getText());
            listaModelUsuarios = controllerUsuario.getListaUsuarioNomeController(modelUsuario);
        } else if (jRBPerfil.isSelected()) {
            modelUsuario.setNomePerfil(jtPesquisa.getText());
            listaModelUsuarios = controllerUsuario.getListaUsuarioPerfilController(modelUsuario);
        }
        DefaultTableModel modelo = (DefaultTableModel) jTUsuarioTabela.getModel();
        modelo.setNumRows(0);
        int cont = listaModelUsuarios.size();
        if (cont == 0) {
            JOptionPane.showMessageDialog(this, "Não foi encontrado nenhum registro!", "Atenção", JOptionPane.WARNING_MESSAGE);
            //carregarUsuarios();
        } else {
            for (int i = 0; i < cont; i++) {
                modelo.addRow(new Object[]{
                    listaModelUsuarios.get(i).getId_usuario(),
                    listaModelUsuarios.get(i).getNome(),
                    listaModelUsuarios.get(i).getNomePerfil(),
                    listaModelUsuarios.get(i).getWhatsapp(),
                    listaModelUsuarios.get(i).getEmail()
                });
            }
        }
    }//GEN-LAST:event_jbBuscaActionPerformed

