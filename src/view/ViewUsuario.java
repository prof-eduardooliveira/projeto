    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int dialogButton = JOptionPane.showConfirmDialog(this, "Deseja excluir o usuário?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (dialogButton == JOptionPane.YES_OPTION) {
            int linha = jTUsuarioTabela.getSelectedRow();
            int codigoUsuario = (int) jTUsuarioTabela.getValueAt(linha, 0);
            if (controllerUsuario.excluirUsuarioController(codigoUsuario)) {
                JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
                this.carregarUsuarios();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o usuário", "ERRO", JOptionPane.ERROR);
            }
        }
        if (dialogButton == JOptionPane.NO_OPTION) {
            remove(dialogButton);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        int linha = jTUsuarioTabela.getSelectedRow();
        int codigo = (int) jTUsuarioTabela.getValueAt(linha, 0);
        perfil = (String) jTUsuarioTabela.getValueAt(linha, 3);
        System.out.println(perfil);
        altcboPerfil = "Alterar";
        listarPerfil(altcboPerfil);
        modelUsuario = controllerUsuario.getUsuarioController(codigo);
        jTCodigo.setText(String.valueOf(modelUsuario.getId_usuario()));
        jTNome.setText(modelUsuario.getNome());
        jTEmail.setText(modelUsuario.getEmail());
        jCBPerfil.getSelectedItem();
        jTUsuario.setText(modelUsuario.getUsuario());
        jTSenha.setText(modelUsuario.getSenha());
        jtZap.setText(modelUsuario.getWhatsapp());
        alterarSalvar = "alterar";
        habilitarDesabilitarCampos(true);
        
        modelPerfil = controllerPerfil.getPerfilController(jCBPerfil.getSelectedItem().toString());
        codigoPerfil = modelPerfil.getCodPerfil();
    }//GEN-LAST:event_jbAlterarActionPerformed

