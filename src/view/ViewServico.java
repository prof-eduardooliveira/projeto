    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int dialogButton = JOptionPane.showConfirmDialog(this, "Deseja excluir o serviço?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (dialogButton == JOptionPane.YES_OPTION) {
            int linha = jTServicoTabela.getSelectedRow();
            int codigoServico = (int) jTServicoTabela.getValueAt(linha, 0);
            if (controllerServico.excluirServicoController(codigoServico)) {
                JOptionPane.showMessageDialog(this, "Serviço excluído com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
                this.carregarServicos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o serviço", "ERRO", JOptionPane.ERROR);
            }
        }
        if (dialogButton == JOptionPane.NO_OPTION) {
            remove(dialogButton);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

