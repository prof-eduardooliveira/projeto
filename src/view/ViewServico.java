    private void carregarServicos() {
        listaModelServicos = controllerServico.getListaServicoController();
        DefaultTableModel modelo = (DefaultTableModel) jTServicoTabela.getModel();
        modelo.setNumRows(0);
        int cont = listaModelServicos.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelServicos.get(i).getCodServico(),
                listaModelServicos.get(i).getDescricao(),
                listaModelServicos.get(i).getPrazo(),
                listaModelServicos.get(i).getPerfil()
            });
        }
    }

