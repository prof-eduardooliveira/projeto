    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try {
            modelServico.setCodServico(Integer.parseInt(jTCodigo.getText()));
        } catch (NumberFormatException e) {
        }
        modelServico.setDescricao(jTDescricaoServ.getText());
        modelServico.setPrazo(Integer.parseInt(jCBPrazo.getSelectedItem().toString()));
        modelServico.setCodPerfil(codigoPerfil);
        //JOptionPane.showMessageDialog(this, "Descrição: " + modelServico.getDescricao() + modelServico.getPrazo() + modelServico.getCodPerfil(), "Atenção", JOptionPane.WARNING_MESSAGE);
        if (alterarSalvar.equals("salvar")) {
            if (controllerServico.salvarServicoController(modelServico) > 0) {
                JOptionPane.showMessageDialog(this, "Serviço cadastrado com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
                carregarServicos();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar o serviço!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (controllerServico.atualizarServicoController(modelServico)) {
                JOptionPane.showMessageDialog(this, "Serviço alterado com sucesso!", "Atenção", JOptionPane.WARNING_MESSAGE);
                carregarServicos();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar o serviço!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

