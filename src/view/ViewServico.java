    // Preenche o combobox
    private void listarPerfis(String tipo){
        if (tipo == "Listar") {
            listaModelPerfis = controllerPerfil.getListaPerfilController();
            jCBPerfil.removeAllItems();
            jCBPerfil.addItem("ESCOLHA");
            for (int i = 0; i < listaModelPerfis.size(); i++) {
                jCBPerfil.addItem(listaModelPerfis.get(i).getNome());
            }
        } else {
            listaModelPerfis = controllerPerfil.getListaPerfilController();
            jCBPerfil.removeAllItems();
            jCBPerfil.addItem(perfil);
            for (int i = 0; i < listaModelPerfis.size(); i++) {
                jCBPerfil.addItem(listaModelPerfis.get(i).getNome());
            }
        }
    }

