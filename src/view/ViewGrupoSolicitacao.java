    private void carregarGruposSolicitacoes() {
        //listaModelServicos = controllerServico.getListaServicoController();
        listModelGrupoSolicitacaos = controllerGrupoSolicitacao.getListaGrupoSolicitacaoController();
        DefaultTableModel modelo = (DefaultTableModel) jTServicoTabela.getModel();
        modelo.setNumRows(0);
        
        for (int i = 0; i < listModelGrupoSolicitacaos.size(); i++) {
            modelo.addRow(new Object[]{
                listModelGrupoSolicitacaos.get(i).getCodGrupoSolicitacao(),
                listModelGrupoSolicitacaos.get(i).getDescricao()
            });
        }
    }

