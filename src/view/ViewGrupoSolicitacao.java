    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        try {
            modelGrupoSolicitacao.setCodGrupoSolicitacao(Integer.parseInt(jTCodigo.getText()));
        } catch (NumberFormatException e) {
        }
        modelGrupoSolicitacao.setDescricao(jTDescricaoGrupoSol.getText());
        //JOptionPane.showMessageDialog(this, "Descrição: " + modelServico.getDescricao() + modelServico.getPrazo() + modelServico.getCodPerfil(), "Atenção", JOptionPane.WARNING_MESSAGE);
        if (alterarSalvar.equals("salvar")) {
            if (controllerGrupoSolicitacao.salvarGrupoSolController(modelGrupoSolicitacao) > 0) {
                JOptionPane.showMessageDialog(this, "Grupo de solicitações cadastrado com sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                carregarGruposSolicitacoes();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
                if(viewSolicitacaoGC != null)
                    viewSolicitacaoGC.listarGruposSolicitacao("Listar");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao cadastrar o grupo de solicitações!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            if (controllerGrupoSolicitacao.atualizarGrupoSolController(modelGrupoSolicitacao)) {
                JOptionPane.showMessageDialog(this, "Grupo de solicitações alterado com sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                carregarGruposSolicitacoes();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
                if(viewSolicitacaoGC != null)
                    viewSolicitacaoGC.listarGruposSolicitacao("Listar");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar o grupo de solicitações!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jbSalvarActionPerformed

