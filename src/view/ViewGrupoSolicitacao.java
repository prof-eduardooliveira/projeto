    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        int dialogButton = JOptionPane.showConfirmDialog(this, "Deseja excluir o grupo selecionado?", "Atenção", JOptionPane.YES_NO_OPTION);
        if (dialogButton == JOptionPane.YES_OPTION) {
            int linha = jTServicoTabela.getSelectedRow();
            int codigoServico = (int) jTServicoTabela.getValueAt(linha, 0);
            if (controllerGrupoSolicitacao.excluirGrupoSolController(codigoServico)) {
                JOptionPane.showMessageDialog(this, "Grupo de solicitações excluído com sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                
                carregarGruposSolicitacoes();
                if(viewSolicitacaoGC != null)
                    viewSolicitacaoGC.listarGruposSolicitacao("Listar");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir o grupo de solicitações", "ERRO", JOptionPane.ERROR);
            }
        }
        if (dialogButton == JOptionPane.NO_OPTION) {
            remove(dialogButton);
        }
    }//GEN-LAST:event_jbExcluirActionPerformed

    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        //jCBPrazo.removeAllItems();
        int linha = jTServicoTabela.getSelectedRow();
        int codigo = (int) jTServicoTabela.getValueAt(linha, 0);
        
        modelGrupoSolicitacao = controllerGrupoSolicitacao.getGrupoSolicitacaoController(codigo);
        jTCodigo.setText(String.valueOf(modelGrupoSolicitacao.getCodGrupoSolicitacao()));
        jTDescricaoGrupoSol.setText(modelGrupoSolicitacao.getDescricao());
        
        alterarSalvar = "alterar";
        habilitarDesabilitarCampos(true);
    }//GEN-LAST:event_jbAlterarActionPerformed

