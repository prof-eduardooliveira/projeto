    private void carregarClientes() {
        listaModelCliente = controllerCliente.retornarListaClienteController();
        DefaultTableModel modelo = (DefaultTableModel) jtCliente.getModel();
        modelo.setNumRows(0);
        // inserir produtos na tabela
        int cont = listaModelCliente.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                listaModelCliente.get(i).getCdc(),
                listaModelCliente.get(i).getNomecsd(),
                listaModelCliente.get(i).getEndereco(),
                listaModelCliente.get(i).getNumero(),
                listaModelCliente.get(i).getBairro(),
                listaModelCliente.get(i).getCidade(),
                listaModelCliente.get(i).getEstado(),
                listaModelCliente.get(i).getNumtel(),
                listaModelCliente.get(i).getEmail()
            });
        }
    }

