    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        int linha = jtCliente.getSelectedRow();
        int codigo = (int) jtCliente.getValueAt(linha, 0);
        modelCliente = controllerCliente.retornarClienteController(codigo);
        jtfCDC.setText(String.valueOf(modelCliente.getCdc()));
        jtfEndereco.setText(modelCliente.getEndereco());
        jtfNomeCSD.setText(modelCliente.getNomecsd());
        jtfNumero.setText(modelCliente.getNumero());
        jtfComplemento.setText(modelCliente.getComplemento());
        jtfBairro.setText(modelCliente.getBairro());
        jcbCidade.addItem(modelCliente.getCidade());
        jtfCEP.setText(modelCliente.getCEP());
        jtfEmail.setText(modelCliente.getEmail());
        jtfNumDoc.setText(modelCliente.getNumdoc());
        jtfTelefone.setText(modelCliente.getNumtel());
        jtfKW_CONT_PT.setText(String.valueOf(modelCliente.getKw_cont_pt()));
        jtfKW_CONT_FPT.setText(String.valueOf(modelCliente.getKw_cont_ftp()));
        alterarSalvar = "alterar";
        habilitarDesabilitarCampos(true);
    }//GEN-LAST:event_jbAlterarActionPerformed

